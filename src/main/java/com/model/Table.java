package com.model;

public class Table {
int id;
int capacity;
boolean available;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getCapacity() {
	return capacity;
}
public void setCapacity(int capacity) {
	this.capacity = capacity;
}
public boolean isAvailable() {
	return available;
}
public void setAvailable(boolean available) {
	this.available = available;
}


}
